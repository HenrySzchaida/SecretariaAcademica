        private void preencherTabela() throws ClassNotFoundException {

        int linha = 0;
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setNumRows( GerenciadorDeArraysEstatico.alunos.size());
        for (Aluno aluno : GerenciadorDeArraysEstatico.alunos) {
            jTable1.setValueAt(aluno.getRa(), linha, 0);
            jTable1.setValueAt(aluno.getNome(), linha, 1);
            jTable1.setValueAt(aluno.getTurma().getNome(), linha, 2);
            
            linha++;
        }
    }

