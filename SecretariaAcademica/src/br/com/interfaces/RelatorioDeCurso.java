    private void preencherTabela() throws ClassNotFoundException {

        int linha = 0;
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setNumRows( GerenciadorDeArraysEstatico.cursos.size());
        for (Curso curso : GerenciadorDeArraysEstatico.cursos) {
            jTable1.setValueAt(curso.getId(), linha, 0);
            jTable1.setValueAt(curso.getNome(), linha, 1);
            
            linha++;
        }
    }

